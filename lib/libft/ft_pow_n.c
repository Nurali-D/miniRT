#include "libft.h"

double	ft_pow_n(int n, int i)
{
	double	num;
	int		f;

	f = 0;
	num = 1;
	if (i < 0)
	{
		i *= -1;
		f = 1;
	}
	while (i)
	{
		num *= n;
		i--;
	}
	if (f == 1)
		num = 1 / num;
	return (num);
}